{
}

void Game::ComposeFrame()
{
	NDCTransformer transformer;
	Matf2 rotMat;
	Vecf2 vec1(0.0, 0.0);
	Vecf2 vec2(0.7, 0.5);
	float test = (float)cos(3.14);
	rotMat = rotMat.Rotation(3.14f);
	vec2 *= rotMat;
	transformer.Transform(vec1);
	transformer.Transform(vec2);
	gfx.DrawLine(vec1, vec2, Colors::White);
}
